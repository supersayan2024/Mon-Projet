
public class Categorie {

	private String le_nom;
	private String une_description;
	
	public Categorie(String le_nom, String une_description) {
		this.le_nom = le_nom;
		this.une_description = une_description;
	}

	public String getLe_nom() {
		return le_nom;
	}

	public void setLe_nom(String le_nom) {
		this.le_nom = le_nom;
	}

	public String getUne_description() {
		return une_description;
	}

	public void setUne_description(String une_description) {
		this.une_description = une_description;
	}
	
	
}
